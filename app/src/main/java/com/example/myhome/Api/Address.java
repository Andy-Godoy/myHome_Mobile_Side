package com.example.myhome.Api;

public class Address {
    private String addressName;
    private int addressNumber;
    private Integer addressFloor;
    private String addressUnit;
    private String addressNeighbourhood;
    private String addressCity;
    private String addressState;
    private String addressCountry;
    private double addressLatitude;
    private double addressLongitude;

    public String getAddressName() {
        return addressName;
    }

    public void setAddressName(String addressName) {
        this.addressName = addressName;
    }

    public int getAddressNumber() {
        return addressNumber;
    }

    public void setAddressNumber(int addressNumber) {
        this.addressNumber = addressNumber;
    }

    public Integer getAddressFloor() {
        return addressFloor;
    }

    public void setAddressFloor(Integer addressFloor) {
        this.addressFloor = addressFloor;
    }

    public String getAddressUnit() {
        return addressUnit;
    }

    public void setAddressUnit(String addressUnit) {
        this.addressUnit = addressUnit;
    }

    public String getAddressNeighbourhood() {
        return addressNeighbourhood;
    }

    public void setAddressNeighbourhood(String addressNeighbourhood) {
        this.addressNeighbourhood = addressNeighbourhood;
    }

    public String getAddressCity() {
        return addressCity;
    }

    public void setAddressCity(String addressCity) {
        this.addressCity = addressCity;
    }

    public String getAddressState() {
        return addressState;
    }

    public void setAddressState(String addressState) {
        this.addressState = addressState;
    }

    public String getAddressCountry() {
        return addressCountry;
    }

    public void setAddressCountry(String addressCountry) {
        this.addressCountry = addressCountry;
    }

    public double getAddressLatitude() {
        return addressLatitude;
    }

    public void setAddressLatitude(double addressLatitude) {
        this.addressLatitude = addressLatitude;
    }

    public double getAddressLongitude() {
        return addressLongitude;
    }

    public void setAddressLongitude(double addressLongitude) {
        this.addressLongitude = addressLongitude;
    }
}
